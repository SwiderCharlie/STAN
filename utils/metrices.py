import torch
import numpy as np


# 损失函数
def huber_loss(preds, labels):
    crit = torch.nn.SmoothL1Loss()
    return crit(preds, labels)


def masked_mse_loss(y_pred, y_true):
    mask = (y_true != 0).float()
    mask /= mask.mean()
    loss = (y_pred - y_true)**2
    loss = loss * mask
    loss[loss != loss] = 0
    return loss.mean()


def masked_mae_loss(y_pred, y_true):
    mask = (y_true != 0).float()
    mask /= mask.mean()
    loss = torch.abs(y_pred - y_true)
    loss = loss * mask
    loss[loss != loss] = 0
    return loss.mean()


